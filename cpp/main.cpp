vector<string> diriterator(string dir)
{
   // open the directory using file operations
   DIR *dirp = opendir(dir.c_str());
   // 
   vector<string> files;

   struct dirent *dp;
   while ((dp = readdir(dirp)) != NULL)
   {
      if (dp->d_name[0] != '.')
      {
         files.push_back(dp->d_name);
      }
   }
   closedir(dirp);
   return files;
}



